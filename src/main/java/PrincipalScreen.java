    private void btnListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnListMouseClicked
//    Valido o evento de duplo clicik
        if (evt.getClickCount() == 2) {
//           Salvo qual foi a UF selecionada
           State selectedUF  =  (State) btnUF.getSelectedItem();
           
//           Salvo qual foi o item selecionado na lista
           int index = btnList.locationToIndex(evt.getPoint());
           County listItem = btnList.getModel().getElementAt(index);
    
//           Instancio o JDialog e chamo a função setMunicipio
//           passando como parâmetro as variáveis salvas anteriormente
           JDialog dialog = new JDialog(this, true);
           dialog.setMunicipio(selectedUF, listItem);
           dialog.setVisible(true);
    }
    }//GEN-LAST:event_btnListMouseClicked

