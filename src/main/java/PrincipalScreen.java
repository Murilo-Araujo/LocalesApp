    private void btnCarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarregarActionPerformed

//        Chamo a função getStates para pegar os dados das UFs
        String statesJson = State.getStates();
        
        JSONArray jsonArray = new JSONArray(statesJson);

//        Converto de Json para objeto e adiciono em btnUf
        for (int i = 0; i < jsonArray.length(); i++) {
            JSONObject jsonObject = jsonArray.getJSONObject(i);
            State state = StateConverter.convert(jsonObject.toString());  
             btnUF.addItem(state);
        }
    }//GEN-LAST:event_btnCarregarActionPerformed

    private void btnUFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUFActionPerformed

    DefaultListModel<County> modelList = new DefaultListModel();
    
//    Removo todos os itens da lista
    modelList.removeAllElements();
        
//    Pego qual foi a UF selecionada
    State seletectedItem  =  (State) btnUF.getSelectedItem();
    
//    Chamo a função para pegar os dados do estado
    String countyJson = County.getCountyByState(seletectedItem.getId());
    
        JSONArray countyArray = new JSONArray(countyJson);
        
//        Faco a conversão de json para objeto e adiciono no modelList
        for (int i = 0; i < countyArray.length(); i++) {
            JSONObject countyObject = countyArray.getJSONObject(i);
            County county = CountyConverter.convert(countyObject.toString());
            modelList.addElement(county);
        }
//        Adiciono o modelList no btnList
    btnList.setModel(modelList);
    }//GEN-LAST:event_btnUFActionPerformed

